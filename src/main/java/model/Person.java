public class Person {
    String name;

    int number;

    public Person() {

    }

    public Person(String name, int number) {
        this.name = name;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

}
